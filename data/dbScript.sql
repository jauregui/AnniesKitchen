
CREATE TABLE Contact (
	idC INT AUTO_INCREMENT PRIMARY KEY,
	firstName VARCHAR(30) NOT NULL,
	lastName VARCHAR(30) NOT NULL,
	telefone VARCHAR(30) NOT NULL,
	mail VARCHAR(50) NOT NULL,
	comment TEXT
);

CREATE TABLE Producto(
	idP INT AUTO_INCREMENT NOT NULL PRIMARY KEY,
	dish VARCHAR(20) NOT NULL,
	description TEXT,
	price INT(5) NOT NULL,
);

CREATE TABLE Menu(
	idM  INT AUTO_INCREMENT NOT NULL PRIMARY KEY,
	week INT(1) NOT NULL,
	FOREIGN KEY(dLu) REFERENCES Producto(idP),
	FOREIGN KEY(dMa) REFERENCES Producto(idP),
	FOREIGN KEY(dMi) REFERENCES Producto(idP),
	FOREIGN KEY(dJu) REFERENCES Producto(idP),
	uPrice INT
);

CREATE TABLE Order(
	idO INT AUTO_INCREMENTNOT NULL PRIMARY KEY,
	FOREIGN KEY(idM) REFERENCES Menu(idM),
	FOREIGN KEY(idC) REFERENCES Contact(idC),
	qLu INT(2),
	qMa INT(2),
	qMi INT(2),
	qJu INT(2),
	total INT(5) NOT NULL
);

